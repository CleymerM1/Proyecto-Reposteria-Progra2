    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // TODO add your handling code here:
        int _codigo;
        String _descripcion="ND";

        //Paso1
            _codigo=Integer.parseInt(txtCodigo.getText());
            
            
            if(this.rbnDescripcion.isSelected()){
            _descripcion="Domicilio";
            }else{
                if(this.rbnDescripcion1.isSelected()){
                _descripcion="Consumo Local";
                }else{
                if(this.rbnDescripcion2.isSelected()){
                  _descripcion="Para Llevar";  
                }
                }
            
    }
             
             //Paso2
             try{
             Servicio servicio=new Servicio();
             //Paso 3
             servicio.set_codigo(_codigo);
             servicio.set_descripcion(_descripcion);
             
        if(this._agregando){
            

            //Paso 4
            MDIPrincipal.gServicio.Agregar(servicio);
            
           
        }else{ //Esta editando los servicios
            MDIPrincipal.gServicio.Modificar(servicio, this._indiceSeleccion);
        }
         //Paso 5
            ActualizarElementosTabla();
            estadoControles(false);
            estadoBotones(true);
            
            //Paso 6
              AdminSerializacion.serializacion(MDIPrincipal.gServicio, "gServicios.obj");

        }catch(Exception e){
             JOptionPane.showMessageDialog(this, "Ha ocurrido un error " + e.getMessage());

        }
    }                                          

    public void ActualizarElementosTabla(){
        String [] titulos={"Codigo", "Tipo de Servicio"};
        DefaultTableModel dt=new DefaultTableModel(MDIPrincipal.gServicio.getArrayGestion(), titulos);
        this.jtServicio.setModel(dt);
    }
     public void estadoControles(boolean _estado){
         this.txtCodigo.setEditable(_estado);
          this.rbnDescripcion.setEnabled(_estado);
         this.rbnDescripcion1.setEnabled(_estado);
         this.rbnDescripcion2.setEnabled(_estado);
     }
         
