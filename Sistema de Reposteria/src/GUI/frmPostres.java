    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // TODO add your handling code here:
        
         /**
             * PASOS
             * 1. Solicitar los valores al usuario
             * 2. Crear la instancia
             * 3. Setear los valores de la instancia
             * 4. Agregar la instancia Postres a la gestion
             * 5. Mostrar los valores
             */
            
            //Paso 1
           
            String  _nombre, _tamanio="ND", _sabor; 
            int _codigo=Integer.parseInt(txtCodigo.getText());
            _nombre=this.txtNombre.getText();
            _sabor=this.txtSabor.getText();
            double _precio=Double.parseDouble(txtPrecio.getText());
            
            if(this.rbnTamanio.isSelected()){
            _tamanio="Grande";
            }else{
                if(this.rbnTamanio1.isSelected()){
                _tamanio="Mediano";
                }else{
                if(this.rbnTamanio2.isSelected()){
                  _tamanio="Peque√±o";  
                }
                }  
            }
            //Paso 2 
            Postres postres=new Postres();
            try{
            //Paso 3
            postres.set_codigo(_codigo);
            postres.set_nombre(_nombre);
            postres.set_tamanio(_tamanio);
            postres.set_sabor(_sabor);
            postres.set_precio(_precio);
            
        if(this._agregando){//Nuevo Postre
           
            //Paso 4
            MDIPrincipal.gPostres.Agregar(postres);
            
            
        }else{ //Esta editando los postres
            MDIPrincipal.gPostres.Modificar(this._indice, postres);
        }
        //Paso 5
            ActualizarElementosTabla();
            estadoControles(false);
            estadoBotones(true);
            
            //Paso 6 Serializar Informacion
            AdminSerializacion.serializacion(MDIPrincipal.gPostres, "gPostres.obj");
         }catch(Exception e) {
             JOptionPane.showMessageDialog(this, "Ha ocurrido un error " + e.getMessage());
         }
    }
        public void ActualizarElementosTabla(){
        String [] titulos={"Codigo", "Nombre", "Tama√±o", "Sabor", "Precio"};
        DefaultTableModel dt=new DefaultTableModel(MDIPrincipal.gPostres.getArrayGestion(), titulos);
        this.jtPostres.setModel(dt);
    }
