    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // TODO add your handling code here:
        //Paso 1
        String _tamanio="ND", _descripcion, _hora;
	 Date _fecha;
        
        _hora=this.txtHora.getText();
        _descripcion=this.txtDescripcion.getText();
         
        if(this.rbntamanio.isSelected()){
            _tamanio="Individual";
            }else{
                if(this.rbntamanio2.isSelected()){
                _tamanio="Pareja";
                }else{
                if(this.rbntamanio3.isSelected()){
                  _tamanio="Familiar";  
                }
                }  
            }
        String dia, mes, anio;
                dia = Integer.toString(this.cboDia.getSelectedIndex());
                mes = Integer.toString(this.cboMes.getSelectedIndex());
                anio = this.txtAnio.getText();

                _fecha = AdminFechas.stringToDate(dia + "-" + mes + "-" + anio);
        //Paso 2
        ReservacionMesas reservacion=new ReservacionMesas();
        
        try{   
    //Paso 3
            reservacion.set_fecha(_fecha);
            reservacion.set_tamanio(_tamanio);
            reservacion.setHora(_hora);
            reservacion.set_descripcion(_descripcion);
            reservacion.set_cliente(_clienteReservacion);
        
        if(this._agregando){
        
        //Paso 4
        MDIPrincipal.gReservacion.Agregar(reservacion);
        
       
        }else{
            MDIPrincipal.gReservacion.Modificar(reservacion, this._indiceSeleccion);
        
        }
        //Paso5
       ActualizarElementosTabla();
            estadoControles(false);
            estadoBotones(true);
            
            //Paso 6 Serializar Informacion
            AdminSerializacion.serializacion(MDIPrincipal.gReservacion, "gReservacion.obj");
         }catch(Exception e) {
             JOptionPane.showMessageDialog(this, "Ha ocurrido un error " + e.getMessage());
         }
        
    }
    public void ActualizarElementosTabla(){
        String[] titulos={"Cliente" , "Tamanio", "Fecha", "Hora","Descripcion"};
        DefaultTableModel dt=new DefaultTableModel(MDIPrincipal.gReservacion.getArrayGestion(),titulos);
        this.jtReservacionMesas.setModel(dt);
    }
