    private void btnClienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnClienteActionPerformed
        String _codigoCliente=JOptionPane.showInputDialog(this, "Código del cliente");
        
        _clienteReservacion=MDIPrincipal.gCliente.obtenerCliente(Integer.parseInt(_codigoCliente));
        
        if(_clienteReservacion==null){
            JOptionPane.showMessageDialog(this,"No se encuentra ese cliente"+ _codigoCliente);
        }else{
            this.lblCliente.setText(_clienteReservacion.get_nombres()+" "+_clienteReservacion.get_apellidos());
        }
    }//GEN-LAST:event_btnClienteActionPerformed

    private void btnReporteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnReporteActionPerformed
        // TODO add your handling code here:
         String nombreArchivo = "Reservacion de Mesas";
        String contenido = MDIPrincipal.gReservacion.getInformacionGestion();
        String ruta = "Reportes\\" + nombreArchivo;

        try {
            FileOutputStream archivo = new FileOutputStream(ruta + ".pdf");
            Document doc = new Document();
            PdfWriter.getInstance(doc, archivo);
            doc.open();
            doc.add(new Paragraph(contenido));
            doc.close();
            JOptionPane.showMessageDialog(null, "Reporte generado correctamente");
        } catch (Exception e) {
            System.out.println("error" + e);
        }
    }//GEN-LAST:event_btnReporteActionPerformed
