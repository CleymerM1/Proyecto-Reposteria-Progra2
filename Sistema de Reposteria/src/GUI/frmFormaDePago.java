    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // TODO add your handling code here:
        /**
             * PASOS
             * 1. Solicitar los valores al usuario
             * 2. Crear la instancia
             * 3. Setear los valores de la instancia
             * 4. Agregar la instancia Forma de Pago a la gestion
             * 5. Mostrar los valores
             */
            
            //Paso 1
           
         int _codigo=Integer.parseInt(txtCodigo.getText());
         String _descripcion="ND";
         
             if(this.rbnDescripcion.isSelected()){
            _descripcion="Tarjeta de Credito";
            }else{
                if(this.rbnDescripcion1.isSelected()){
                _descripcion="Tarjeta de Debito";
                }else{
                if(this.rbnDescripcion2.isSelected()){
                  _descripcion="Efectivo";  
                }
                }  
            }
            
            //Paso 2 
        Forma_De_Pago formapago=new Forma_De_Pago();
            try{
            //Paso 3
           formapago.set_codigo(_codigo);
           formapago.set_descripcion(_descripcion);
          
           
        if(this._agregando){//Nueva Forma de Pago
            
            //Paso 4
            MDIPrincipal.gForma_De_Pago.Agregar(formapago);
            
            
        }else{ //Esta editando las formas de pago
            MDIPrincipal.gForma_De_Pago.Modificar(formapago, this._indiceSeleccion);
        }
        //Paso 5
            ActualizarElementosTabla();
            estadoControles(false);
            estadoBotones(true);
            
            //Paso 6 Serializar Informacion
            AdminSerializacion.serializacion(MDIPrincipal.gForma_De_Pago, "gFormaDePago.obj");
         }catch(Exception e) {
             JOptionPane.showMessageDialog(this, "Ha ocurrido un error " + e.getMessage());
         }
    }                                          

    public void ActualizarElementosTabla(){
        String [] titulos={"Codigo", "Metodo"};
        DefaultTableModel dt=new DefaultTableModel(MDIPrincipal.gForma_De_Pago.getArrayGestion(), titulos);
        this.jtFormaDePago.setModel(dt);
    }
           public void estadoControles(boolean _estado){
         this.txtCodigo.setEditable(_estado);
         this.rbnDescripcion2.setEnabled(_estado);
         this.rbnDescripcion.setEnabled(_estado);
         this.rbnDescripcion1.setEnabled(_estado);
         
         
     }
         
